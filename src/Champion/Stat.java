package Champion;

public class Stat {
	private String champname;
	private String hp;
	private String mp;
	private String attack;
	private String attack_speed;
	private String armor;
	private String magic_resistance;
	private String speed;
	private String attack_range;
	
	public String getChampname() {
		return champname;
	}
	public void setChampname(String champname) {
		this.champname = champname;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getMp() {
		return mp;
	}
	public void setMp(String mp) {
		this.mp = mp;
	}
	public String getAttack() {
		return attack;
	}
	public void setAttack(String attack) {
		this.attack = attack;
	}
	public String getAttack_speed() {
		return attack_speed;
	}
	public void setAttack_speed(String attack_speed) {
		this.attack_speed = attack_speed;
	}
	public String getArmor() {
		return armor;
	}
	public void setArmor(String armor) {
		this.armor = armor;
	}
	public String getMagic_resistance() {
		return magic_resistance;
	}
	public void setMagic_resistance(String magic_resistance) {
		this.magic_resistance = magic_resistance;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getAttack_range() {
		return attack_range;
	}
	public void setAttack_range(String attack_range) {
		this.attack_range = attack_range;
	}
	
}
